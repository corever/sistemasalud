        <br/><br/>
        Algunos ejemplos son:<br/>
        Cantidad de kilos decomisados<br/>
        Nombre de sustancias químicas mal almacenadas<br/>
        Cantidad de sustancias químicas mal almacenadas<br/><br/>
        Puede agregar cuantos datos adicionales considere necesario con la posibilidad de editar y eliminar posteriormente.<br/>
        <strong>Importante:</strong> Una vez iniciada la fecha de campaña, esta no podrá ser editada.
